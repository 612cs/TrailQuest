def set_text(paragraph, text):
    run_style = None
    if paragraph.runs:
        src = paragraph.runs[0]
        run_style = {
            "bold": src.bold,
            "italic": src.italic,
            "underline": src.underline,
            "name": src.font.name,
            "size": src.font.size,
        }
    paragraph.clear()
    run = paragraph.add_run(text)
    if run_style:
        run.bold = run_style["bold"]
        run.italic = run_style["italic"]
        run.underline = run_style["underline"]
        run.font.name = run_style["name"]
        run.font.size = run_style["size"]


