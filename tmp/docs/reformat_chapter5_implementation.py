def replace_text_keep_run_style(paragraph, text):
    first_run = paragraph.runs[0] if paragraph.runs else None
    p_pr = deepcopy(paragraph._p.pPr) if paragraph._p.pPr is not None else None
    for child in list(paragraph._p):
        paragraph._p.remove(child)
    if p_pr is not None:
        paragraph._p.append(p_pr)
    run = paragraph.add_run(text)
    if first_run is not None:
        run.bold = first_run.bold
        run.italic = first_run.italic
        run.underline = first_run.underline
        run.font.name = first_run.font.name
        run.font.size = first_run.font.size


doc = Document(DOCX)

for para in doc.paragraphs:
    old = para.text.strip()
    if old in REPLACEMENTS:
        replace_text_keep_run_style(para, REPLACEMENTS[old])

doc.save(DOCX)
